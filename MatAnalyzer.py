    async def matscmd(self, message: Message):
        """— статистика матов в чате (только русские маты)"""
        msg = await utils.answer(message, self.strings["analyzing"])
        
        try:
            chat = await message.get_chat()
            
            all_mats = Counter()
            user_mats = Counter()
            my_mats_count = 0
            total_mats = 0
            
            async for msg_obj in self.client.iter_messages(chat, limit=10000):
                # Безопасно получаем текст сообщения
                text = None
                
                if hasattr(msg_obj, 'message') and msg_obj.message is not None:
                    text = msg_obj.message
                elif hasattr(msg_obj, 'text') and msg_obj.text is not None:
                    text = msg_obj.text
                elif hasattr(msg_obj, 'raw_text') and msg_obj.raw_text is not None:
                    text = msg_obj.raw_text
                
                if not text:
                    continue
                
                # Приводим к нижнему регистру
                text_lower = str(text).lower()
                
                # Ищем маты - только отдельные слова
                words = re.findall(r'\b\w+\b', text_lower)
                found_mats = []
                
                for word in words:
                    # Проверяем, является ли слово матом
                    for mat_word in self.mat_words:
                        if word.startswith(mat_word):
                            found_mats.append(word)
                            break
                
                # Проверяем, есть ли маты и sender_id
                if found_mats and msg_obj.sender_id:
                    sender_id = msg_obj.sender_id
                    
                    for mat in found_mats:
                        # Добавляем в статистику
                        all_mats[mat] += 1
                        user_mats[sender_id] += 1
                        total_mats += 1
                    
                    # Считаем мои маты
                    if sender_id == message.sender_id:
                        my_mats_count += len(found_mats)
                # Если маты есть, но нет sender_id
                elif found_mats:
                    for mat in found_mats:
                        all_mats[mat] += 1
                        total_mats += 1
            
            # Проверяем, найдены ли маты
            if total_mats == 0:
                await utils.answer(msg, self.strings["no_mats"])
                return
            
            # Рассчитываем проценты
            my_percent = round((my_mats_count / total_mats) * 100, 2) if total_mats > 0 else 0
            
            # Формируем топ-10 матов
            top_words = ""
            for i, (word, count) in enumerate(all_mats.most_common(10), 1):
                top_words += f"{i}. {word}: <code>{count}</code>\n"
            
            # Формируем топ пользователей
            top_users = ""
            for i, (user_id, count) in enumerate(user_mats.most_common(10), 1):
                try:
                    user = await self.client.get_entity(user_id)
                    if user.username:
                        name = f"@{user.username}"
                    elif user.first_name:
                        name = user.first_name
                        if user.last_name:
                            name += f" {user.last_name}"
                    else:
                        name = f"ID: {user_id}"
                except Exception:
                    name = f"ID: {user_id}"
                
                top_users += f"{i}. {name}: <code>{count}</code>\n"
            
            # Формируем финальное сообщение
            result = self.strings["mats"].format(
                total=total_mats,
                my_mats=my_mats_count,
                my_percent=my_percent,
                unique=len(all_mats),
                top_words=top_words,
                top_users=top_users
            )
            
            await utils.answer(msg, result)
            
        except Exception as e:
            # Подробный вывод ошибки для отладки
            import traceback
            error_msg = f"{self.strings['error']}:\n{type(e).__name__}: {str(e)}\n\n{traceback.format_exc()}"
            await utils.answer(msg, error_msg[:4000])